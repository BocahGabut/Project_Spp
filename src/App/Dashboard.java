    void hide_panel(JPanel panel,JLabel label)
    {
        panel.setVisible(false);
        label.setText("Show");
        
        ImageIcon myimage = new ImageIcon(Toolkit.getDefaultToolkit().getImage("src/Image/icons8_Chevron_Down_15px.png"));
        
        Image img1 = myimage.getImage();
        Image img2 = img1.getScaledInstance(15, 17, Image.SCALE_SMOOTH);
        ImageIcon i = new ImageIcon(img2);
        
        jLabel27.setIcon(i);

    }
    
    void Image()
    {
        
        if(jLabel26.getText() == "Show")
        {
            image = "src/Image/icons8_Chevron_Up_15px.png";
        }
        else
        {
            image = "src/Image/icons8_Chevron_Down_15px.png";
        }
        
        ImageIcon myimage = new ImageIcon(Toolkit.getDefaultToolkit().getImage(image));
        
        Image img1 = myimage.getImage();
        Image img2 = img1.getScaledInstance(15, 17, Image.SCALE_SMOOTH);
        ImageIcon i = new ImageIcon(img2);
        
        jLabel27.setIcon(i);

    }
    
