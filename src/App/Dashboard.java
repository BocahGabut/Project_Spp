    public void setJam(){
        ActionListener taskPerformer = new ActionListener() {

        public void actionPerformed(ActionEvent evt) {
        String nol_jam = "", nol_menit = "",nol_detik = "";

        java.util.Date dateTime = new java.util.Date();
        int nilai_jam = dateTime.getHours();
        int nilai_menit = dateTime.getMinutes();
        int nilai_detik = dateTime.getSeconds();

        if(nilai_jam <= 9) nol_jam= "0";
        if(nilai_menit <= 9) nol_menit= "0";
        if(nilai_detik <= 9) nol_detik= "0";

        String waktu = nol_jam + Integer.toString(nilai_jam);
        String menit = nol_menit + Integer.toString(nilai_menit);
        String detik = nol_detik + Integer.toString(nilai_detik);
        Date date = new Date();
        SimpleDateFormat s = new SimpleDateFormat("dd MMMM YYYY");
        
        lbl_jam.setText(waktu+":"+menit + ":" + detik);
        lbl_tgl.setText(s.format(date));
        
        }
    };
        new Timer(1000, taskPerformer).start();
    }
    
    private void Edit_User()
    {
        try 
        {
            Statement stat = (Statement) db_connection.getKoneksi().createStatement();
            String sql = "Select * from user where username = '"+jLabel21.getText()+"'";
            ResultSet res = stat.executeQuery(sql);
            
            while(res.next())
            {
                Edit_User edit = new Edit_User();
                Edit_User.username.setText(""+res.getString("username"));
                Edit_User.nama.setText(""+res.getString("nama"));
                
                edit.setVisible(true);
            }
            
        } 
        catch (SQLException ex) 
        {
            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }
    
