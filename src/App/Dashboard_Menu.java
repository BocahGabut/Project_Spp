    {
        Show_Setting();
        
        ImageIcon myimage = new ImageIcon(Toolkit.getDefaultToolkit().getImage(path));
        
        Image img1 = myimage.getImage();
        Image img2 = img1.getScaledInstance(318, 186, Image.SCALE_SMOOTH);
        ImageIcon i = new ImageIcon(img2);
        
        jLabel1.setIcon(i);
    }
    
