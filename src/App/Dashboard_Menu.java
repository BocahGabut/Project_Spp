    private void jumlah_pengguna()
    {
        try 
        {
            Statement stat = (Statement) db_connection.getKoneksi().createStatement();
            String sql = "Select count(username) from user";
            ResultSet res = stat.executeQuery(sql);
            
            while(res.next())
            {
                jLabel4.setText(res.getString(WIDTH));
            }
            
        } 
        catch (SQLException ex) 
        {
            Logger.getLogger(Dashboard_Menu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
